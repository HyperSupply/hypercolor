const colors = [
  'blue-gray',
  'cool-gray',
  'true-gray',
  'warm-gray',
  'gray',
  'orange',
  'amber',
  'yellow',
  'lime',
  'emerald',
  'teal',
  'green',
  'cyan',
  'sky',
  'blue',
  'violet',
  'purple',
  'fuchsia',
  'indigo',
  'rose',
  'red',
]

const shades = [50, 100, 200, 300, 400, 500, 600, 700, 800, 900]

function allColors(key, extra) {
  let allColors = colors.map(function(color) {
    return shades.map((shade) => `${key}-${color}-${shade}`)
  })

  allColors = [...allColors, `${key}-black`, `${key}-white`, extra]

  return allColors.flat()
}

export const fromColors = allColors('from')
export const viaColors = allColors('via', 'none')
export const toColors = allColors('to')
